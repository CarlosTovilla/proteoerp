	/**
	* Busca la data en el Servidor por json
	*/
	function getdata()
	{
		$grid       = $this->jqdatagrid;

		// CREA EL WHERE PARA LA BUSQUEDA EN EL ENCABEZADO
		$mWHERE = $grid->geneTopWhere('tarifa');

		$response   = $grid->getData('tarifa', array(array()), array(), false, $mWHERE, 'actividad' );
		$rs = $grid->jsonresult( $response);
		echo $rs;
	}

	/**
	* Guarda la Informacion
	*/
	function setData()
	{
		$this->load->library('jqdatagrid');
		$oper   = $this->input->post('oper');
		$id     = $this->input->post('id');
		$data   = $_POST;
		$mcodp  = "id";
		$check  = 0;

		unset($data['oper']);
		unset($data['id']);
		if($oper == 'add'){
			if(false == empty($data)){
				$this->db->insert('tarifa', $data);
				echo "Registro Agregado";
				logusu('TARIFA',"Registro  INCLUIDO");
			} else
				echo "Fallo Agregado!!!";

		} elseif($oper == 'edit') {
			$this->db->where("id", $id);
			$this->db->update('tarifa', $data);
			logusu('TARIFA',"Grupo de Cliente  ".$id." MODIFICADO");
			echo "$mcodp Modificado";

		} elseif($oper == 'del') {
			$meco = $this->datasis->dameval("SELECT actividad FROM tarifa WHERE id=$id");
			$this->db->simple_query("DELETE FROM tarifa WHERE id=$id ");
			logusu('TARIFA',"Registro $meco ELIMINADO");
			echo "Registro Eliminado";
		};
	}

